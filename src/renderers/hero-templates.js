/**
 * H1-generated - Full-bleed hero slide (AI generates everything)
 * This is essentially L29 but tagged as a frontend template.
 * Used when user wants AI to generate the entire title slide design.
 */
function renderH1Generated(content, slide = {}, slideIndex = 0) {
  // Extract background settings
  const backgroundColor = slide?.background_color || content?.background_color || '#1f2937';
  const backgroundImage = slide?.background_image || content?.background_image || '';

  // Build background style
  let backgroundStyle = '';
  if (backgroundImage) {
    backgroundStyle = `background-image: url('${backgroundImage}'); background-size: cover; background-position: center; background-repeat: no-repeat;`;
    if (backgroundColor) {
      backgroundStyle += ` background-color: ${backgroundColor};`;
    }
  } else if (backgroundColor) {
    backgroundStyle = `background-color: ${backgroundColor};`;
  }

  return `
    <section data-layout="H1-generated" data-template="H1-generated" class="hero-slide grid-container" style="${backgroundStyle}">
      <!-- Hero Content Area (full-bleed) -->
      <div class="hero-content-area"
           data-slot-type="hero"
           data-section-id="slide-${slideIndex}-section-hero"
           data-section-type="hero"
           data-slide-index="${slideIndex}"
           style="grid-row: 1/19; grid-column: 1/33; overflow: hidden; width: 100%; height: 100%;"
           data-content-width="1920px"
           data-content-height="1080px">
        ${content.hero_content || ''}
      </div>
    </section>
  `;
}

